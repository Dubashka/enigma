import streamlit as st


def render() -> None:
    st.header("Как пользоваться сервисом")

    st.subheader("1. Маскирование данных")
    st.markdown(
        """
        1. Перейдите на вкладку **Маскирование**.
        2. Загрузите файл Excel (.xlsx) или CSV.
        3. Выберите листы и столбцы, которые нужно замаскировать.
        4. Нажмите **Маскировать** — сервис заменит чувствительные данные на безопасные подстановки.
        5. Скачайте замаскированный файл и файл-маппинг (JSON).
        """
    )

    st.subheader("2. Работа с LLM")
    st.markdown(
        """
        - Отправьте **замаскированный** файл в облачную LLM (ChatGPT, Claude и т.д.).
        - Модель работает с обезличенными данными — конфиденциальная информация не покидает периметр.
        - Получите результат от LLM и сохраните его.
        """
    )

    st.subheader("3. Дешифровка")
    st.markdown(
        """
        1. Перейдите на вкладку **Дешифровка**.
        2. Загрузите файл-результат от LLM и файл-маппинг (JSON), полученный на шаге маскирования.
        3. Нажмите **Дешифровать** — сервис восстановит оригинальные значения.
        4. Скачайте восстановленный файл.
        """
    )

    st.divider()

    st.markdown(
        """
        **Есть вопросы?** Пишите: [ilya.mukha@reksoft.com](mailto:ilya.mukha@reksoft.com)
        """
    )
