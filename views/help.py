import streamlit as st


def render() -> None:
    st.header("Как пользоваться сервисом")

    st.subheader("1. Маскирование данных")
    st.markdown(
        """
        1. Перейдите на вкладку **Маскирование**.
        2. Загрузите файл Excel (.xlsx) или CSV.
        3. Выберите листы и столбцы, которые нужно замаскировать.
        4. Нажмите **Маскировать** — сервис заменит чувствительные данные на безопасные подстановки.
        5. Скачайте замаскированный файл и файл-маппинг (JSON).
        """
    )

    st.subheader("2. Работа с LLM")
    st.markdown(
        """
        - Отправьте **замаскированный** файл в облачную LLM (ChatGPT, Claude и т.д.).
        - Модель работает с обезличенными данными — конфиденциальная информация не покидает периметр.
        - Получите результат от LLM и сохраните его.
        """
    )

    st.subheader("3. Дешифровка")
    st.markdown(
        """
        1. Перейдите на вкладку **Дешифровка**.
        2. Загрузите файл-результат от LLM и файл-маппинг (JSON), полученный на шаге маскирования.
        3. Нажмите **Дешифровать** — сервис восстановит оригинальные значения.
        4. Скачайте восстановленный файл.
        """
    )

    st.subheader("4. Конвертация сканированных PDF")
    st.markdown(
        """
        1. Перейдите на вкладку **Конвертация в MD**.
        2. Загрузите PDF-файл.
        3. Если PDF является **сканом** (состоит из изображений страниц),
           включите чекбокс **«Это сканированный PDF (использовать OCR)»**.
        4. Выберите язык распознавания (русский+английский, только русский, только английский).
        5. Нажмите **Конвертировать** — сервис распознает текст через Tesseract OCR.
        6. Скачайте результат в нужном формате: **.txt**, **.md** или **.json**.
        """
    )

    st.subheader("⚙️ Установка Tesseract (для OCR)")
    st.markdown(
        """
        OCR-функция требует наличия **Tesseract** на сервере/машине, где запущен сервис.
        Установите его одним из способов:

        **Linux (Ubuntu / Debian):**
        ```bash
        sudo apt install tesseract-ocr tesseract-ocr-rus
        ```

        **macOS (Homebrew):**
        ```bash
        brew install tesseract
        brew install tesseract-lang  # все языки, включая русский
        ```

        **Windows:**
        Скачайте установщик с [github.com/UB-Mannheim/tesseract](https://github.com/UB-Mannheim/tesseract/wiki)
        и добавьте путь к `tesseract.exe` в системную переменную `PATH`.
        Языковой пакет для русского: выберите `rus` в процессе установки.

        После установки проверьте, что Tesseract доступен:
        ```bash
        tesseract --version
        ```
        """
    )

    st.divider()

    st.markdown(
        """
        **Есть вопросы?** Пишите: [ilya.mukha@reksoft.com](mailto:ilya.mukha@reksoft.com)
        """
    )
